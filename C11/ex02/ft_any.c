int	ft_any(char **tab, int (*f)(char *))
{
	int	i;
	int	result;

	result = 0;
	i = 0;
	while (tab[i])
	{
		if ((*f)(tab[i]) != 0)
		{
			result = 1;
			break ;
		}
		i++;
	}
	return (result);
}
